#include "GeometryData.hpp"

ModelData::ModelData(std::vector<Vertex> data, DEFAULT_MODEL type)
{
    this->data = data;
    this->type = type;
}

std::unordered_map<DEFAULT_MODEL, ModelData> GeometryData::defaultModelDatas;

ModelData GeometryData::GetModelData(DEFAULT_MODEL model)
{
    ModelData* newModelData = nullptr;
    std::vector<Vertex> vertices;
    
    switch (model)
    {
    case CUBE:

        if(GeometryData::defaultModelDatas.find(CUBE) == GeometryData::defaultModelDatas.end()) 
        {
           vertices = GetVertexVecFromData(GetVectorFromFloatArr<glm::vec3>(cubeVertices,3,0,36),
           GetVectorFromFloatArr<glm::vec3>(cubeVertices,3,3,36),GetVectorFromFloatArr<glm::vec2>(cubeVertices,2,6,36));
           
           ModelData m_Data = ModelData(vertices,CUBE);

           GeometryData::defaultModelDatas[CUBE] = m_Data;
           newModelData = &GeometryData::defaultModelDatas[CUBE];
        }

        else
        {
            newModelData = &GeometryData::defaultModelDatas[CUBE];
        }
        
        break;
    
    default:
        break;
    }

    return *newModelData;
}

std::vector<Vertex> GeometryData::GetVertexVecFromData(std::vector<glm::vec3> positions,
    std::vector<glm::vec3> normals,std::vector<glm::vec2> textCords)
{
    std::vector<Vertex> vertex;

    if (positions.size() != normals.size() || positions.size() != textCords.size()) {
        std::cout << "Vertex data is wrong" << std::endl;
    }

    for(unsigned int i = 0; i < positions.size(); i++)
    {
        Vertex v;
        v.position = positions[i];
        v.normal = normals[i];
        v.textcords = textCords[i];
        vertex.push_back(v);
    }

    return vertex;
}

